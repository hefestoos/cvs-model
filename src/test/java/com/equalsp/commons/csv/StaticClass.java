package com.equalsp.commons.csv;

public class StaticClass {

	private int number;

	private Integer numberObj;
	
	private double realNumber;

	private Double realNumberObj;
	
	private boolean trueOrFalse;

	private boolean trueOrFalseObj;

	private char letter;

	private Character letterObj;
	
	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public Integer getNumberObj() {
		return numberObj;
	}

	public void setNumberObj(Integer numberObj) {
		this.numberObj = numberObj;
	}

	public double getRealNumber() {
		return realNumber;
	}

	public void setRealNumber(double realNumber) {
		this.realNumber = realNumber;
	}

	public Double getRealNumberObj() {
		return realNumberObj;
	}

	public void setRealNumberObj(Double realNumberObj) {
		this.realNumberObj = realNumberObj;
	}

	public boolean isTrueOrFalse() {
		return trueOrFalse;
	}

	public void setTrueOrFalse(boolean trueOrFalse) {
		this.trueOrFalse = trueOrFalse;
	}

	public boolean isTrueOrFalseObj() {
		return trueOrFalseObj;
	}

	public void setTrueOrFalseObj(boolean trueOrFalseObj) {
		this.trueOrFalseObj = trueOrFalseObj;
	}

	public char getLetter() {
		return letter;
	}

	public void setLetter(char letter) {
		this.letter = letter;
	}

	public Character getLetterObj() {
		return letterObj;
	}

	public void setLetterObj(Character letterObj) {
		this.letterObj = letterObj;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	private String text;
	
	
}
